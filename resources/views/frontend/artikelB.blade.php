          <p class="lead">Anjing kesayangan mempunyai peran tersendiri dalam hidup. Banyak yang menganggap mereka sebagai sahabat, saudara, dan sebagainya. Jadi, kesehatan mereka itu penting untuk diperhatikan. Apa saja cara untuk menjaga mereka tetap dalam kondisi yang baik? Baca tips-tips berikut, PetLovers.</p>
          <h4>Cek kesehatannya setiap hari</h4>
          <p>Berbagai penyakit anjing bisa saja menghampiri hewan kesayanganmu. Sudah menjadi kewajiban untuk selalu memeriksa kesehatannya setiap hari. Anjing tidak dapat berkomunikasi dengan baik ketika mereka merasa ada yang tidak beres dengan tubuh mereka, jika kamu melihat anjingmu memberi tanda-tanda yang ganjil sebaiknya konsultasikan dengan dokter hewan terdekat.</p>
          <h4>Pola makan seimbang</h4>
          <p>Tidak sulit untuk menemukan makanan anjing yang bagus. Namun, dalam memilih pakan yang tepat kamu harus memperhatikan komposisi pada makanannya. Berikan makan dengan teratur dan porsi yang cukup. Hal tersebut penting untuk menghindari obesitas.</p>
          <h4>Berikan air putih</h4>
          <p>Sama seperti manusia, anjing butuh cukup air pada tubuhnya. Berikan ia air setelah makan dan beraktivitas. Jangan lupa untuk mengganti airnya setiap hari. Air yang tidak diganti bisa tercemar oleh bakteri dan virus. Kamu tidak ingin mereka sakit, bukan?</p>
          <h4>Grooming yang teratur</h4>
          <p>Grooming teratur sangat baik untuk kesehatannya. Selain menghindari penyakit kulit, bulu yang terawat membuat mereka dan kamu pun nyaman. Bawa anjing kesayanganmu grooming 3-6 minggu sekali untuk menjaga kesehatan kulit dan bulunya.</p>
          <h4>Olahraga teratur</h4>
          <p>Selain memberi makanan anjing yang berkualitas, olahraga juga dapat menjaga mereka tetap sehat. Mengajak anjing kesayanganmu jalan-jalan di sore/pagi hari dapat menjadi alternatif yang mudah. Tak hanya itu, dengan olahraga anjingmu bisa mempunyai berat badan yang ideal serta memperlancar peredaran darah dan menghindarinya dari kebosanan. Selain jalan-jalan, kamu juga bisa mengajak mereka bermain bola di halaman rumah. Seru kan, PetLovers?</p>
          <p>Itulah 5 tips untuk menjaga anjing kesayangan tetap sehat. Selalu berikan waktu untuk memperhatikan segala kebutuhan mereka. Jika anjingmu sehat dan bahagia, kamu pun juga merasa hal yang sama bukan?</p>

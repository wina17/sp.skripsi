          <p class="lead">Pernahkah petlovers memergoki hewan kesayangannya tidak nyaman karena gatal? Bisa jadi hewan kesayangan petlovers terkena scabies, lalu bagaimana cara mengatasinya?</p>
          <p>Selain mengganggu hewan kesayangan karena rasa gatal yang menyerang, petlovers pun akan merasa tidak nyaman saat melihat penyakit ini menyerang hewan kesayangan, apalagi jika sudah terserang cukup parah.</p>
          <p>Menurut Drh. Feby Yolanda, scabies bisa terkena pada kucing dan anjng disebabkan adanya kontak fisik secara langsung dengan hewan lainnya yang sudah terserang lebih dahulu, disamping itu juga bisa tertular dari benda-benda atau perlengkapan yang dipakai pada saat hewan grooming bekas dipakai oleh hewan yang terkena scabies.</p>
          <p>Ternyata tidak hanya kucing dan anjing, penyakit ini dapat menyerang kelinci dan musang yang petlovers pelihara. Penyakit ini juga dapat menyerang semua usia, jenis kelamin (betina maupun jantan) pada kucing dan anjing. Selain itu scabies bisa ditularkan melalui benda yang digunakan bersama-sama seperti handuk atau sabun mandi.</p>
          <blockquote class="blockquote">
            <p class="mb-0">“Tanpa inang tungau ini hanya bisa hidup selama 2-3 hari, namun dia akan bertahan hidup hingga 2 bulan apabila sudah menginfeksi inang (manusia/hewan) dan bisa lebih jika kondisi inang sedang tidak sehat,”</p>
            <footer class="blockquote-footer">Drh. Feby Yolanda
            </footer>
          </blockquote>
          <h4>Apa itu scabies?</h4>
          <p>Feby menuturkan, “penyakit scabies pada kucing dan anjing disebabkan oleh parasit atau tungau yang bernama Notoedres cati (Kucing) dan Sarcoptes scabiei (anjing), tungau ini memiliki ukuran yang sangat kecil”.</p>
          <p>Karena ukurannya yang sangat kecil yaitu 0.2 – 0.4 mm maka untuk mendiagnosa tungau tersebut tidak bisa dilakukan dengan mata telanjang tetapi harus memakai bantuan mikroskop atau kaca pembesar dengan melihat kerokan kulit yang diambil didaerah kulit yang mengalami perubahan.
          <h4>Gejala Klinis</h4>
          <p>Petlovers dapat mengenali gejala klinis pada hewan peliharaan apabila terkena scabies. Ada beberapa ciri yang sangat mudah dilihat seperti menggaruk-garuk bagian tertentu cukup intens pada badan hewan, biasanya sekitar telinga</p>
          <p>Akibatnya akan membuat kerusakan pada kulit sehingga kulit menjadi tergores-gores, memerah dan berkerak yang dapat mengundang bakteri atau jamur masuk. Adanya bakteri dan jamur yang masuk sebagai infeksi sekunder dapat memperparah kegatalan yang dialami oleh hewan.</p>
          <p>Kerusakan kulit yang terlihat paling khas pada penyakit ini yakni adanya keropeng dan kemerahan pada tepi daun telinga, siku dan ujung-ujung jari kaki. Pada kondisi parah akan membuat rambut menjadi rontok sampai botak serta kulit diseluruh badan menjadi memerah dan berkerak.</p>
          <h4>Antisipasi Pencegahan</h4>
          <p>Menjaga kebersihan lingkungan terutama hewan kesayangan. Segera melakukan pengobatan apabila kondisi gatal sudah terlalu lama dan terjadi dibanyak tempat agar segera dilakukan pengobatan. Apabila hewan kesayangan/ owner terkena scabies dan dilakukan pengobatan sebaiknya disertai dengan desinfeksi lingkungan sekitar agar tungau di lingkungan dapat dibasmi dan tidak menginfeksi lagi.
          </p>
          <p>Selain itu, pemilik juga dapat menghindari hewannya kontak dengan sumber di mana parasit berada, seperti kontak dengan hewan liar yang terkena penyakit ini atau kontak dengan peralatan mandi bekas pakai hewan yang menderita.</p>
          <p>Bagi pemilik, hindari kontak dengan hewan lain yang terkena scabies. Jika terlanjur kontak, maka pemilik harus segera mencuci tangan dan mengganti pakaian jika ingin kontak dengan hewannya. Serta yang tidak boleh dilupakan adalah hindari tempat grooming atau penitipan hewan yang tidak mempunyai kebersihan yang cukup baik</p>
